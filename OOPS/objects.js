//In object oriented programming concept we group related variables and functions (properties and methods) under same group called object

//creating an object

let man={
    name:'Karan',
    age:26,
    work:function(){
        return 'Hi, I am a software engineer';
    }
}

//accessing properties

console.log(man.name);

console.log(man.age);

console.log(man.work());